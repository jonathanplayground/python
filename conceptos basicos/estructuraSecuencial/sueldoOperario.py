#Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor por hora

valorHora = int(input("Digite el valor por hora del operario: "))
horaTrabajada = int(input("Digite las horas trabajadas: "))

sueldo = valorHora * horaTrabajada

print("El sueldo del trabajaor es: ")
print(sueldo)